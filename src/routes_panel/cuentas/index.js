	componentDidMount() {
		this.fetch({
			limite: 10,
			pagina: 1,
		});
	}


	fetch = async (params = {}) => {
		this.setState({ cuentas: [] })
		try {
			const response = await api.cuentas.getAll(sessionStorage.getItem("lugar_id"), queryString.stringify({
				...params,
				query: this.state.q,
			}))
			if (response.status === "success") {
				this.setState({
					cuentas: response.data.cuentas
				})
			}
		} catch (e) {
			message.error(e.toString(), 7);
		} finally {
			this.setState({
				confirmLoading: false,
			})
		}
	}

	MercadoPago = async () => {
		this.setState({ isLoading: true })
		const CUSTOMIZED_ID = sessionStorage.getItem('lugar_id')
		sessionStorage.setItem('CUSTOMIZED_ID', CUSTOMIZED_ID)
		try {
			this.setState({ openModal: true })
			window.open(`https://auth.mercadopago.com.ar/authorization?client_id=${config.APP_ID_MP}&response_type=code&platform_id=mp&state=id=${CUSTOMIZED_ID}=&redirect_uri=https://www.vivircarlospaz.com`, "_self")
		} catch (error) {
			message.error(error)
		}
	}

	onChange = async (e, data) => {
		if (e === true) {
			const response = await api.cuentas.activar(data.idlugar, data.id)
			if (response.status === "success") {
				message.success(response.message)
				this.fetch()
			} else {
				message.error(response.message)
			}
		} else {
			const response = await api.cuentas.desactivar(data.id)
			if (response.status === "success") {
				message.success(response.message)
				this.fetch()
			} else {
				message.error(response.message)
			}
		}
	}

	showConfirmar = (id) => {
		const _this = this;
		Modal.confirm({
			title: '¿Está seguro que desea ELIMINAR?',
			okText: 'Eliminar',
			okType: 'danger',
			cancelText: 'Cancelar',
			async onOk() {
				_this.eliminarCuenta(id)
			},
			onCancel() {
				console.log('Cancel');
			},
		});
	}
	eliminarCuenta = async (id) => {
		try {
			const response = await api.cuentas.eliminar(id)
			if (response.status === "success") {
				message.success("Cuenta eliminada con éxito");
				this.fetch();
			}
		} catch (error) {
			message.error(error)
		}
	}

	renovarCuenta = async (refresh_token) => {
		this.setState({loadingButton:true})
		try {
			const response = await api.cuentas.renovar({
				client_secret: config.CLIENT_SECRET_MP,
				grant_type:'refresh_token',
				refresh_token:refresh_token,
			})
			if (response.status === "success") {
				message.success("Cuenta renovada con éxito");
				this.fetch();
				this.setState({loadingButton:false})
			}
		} catch (error) {
			this.setState({loadingButton:false})
		}
	}

